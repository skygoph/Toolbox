        [HttpPost, CheckSessionOut]
        public ActionResult Upload(ViewMemo vm)
        {
                        
            string msg = string.Empty;                    

            try
            {
                if (vm.file.ContentType.ToString().Contains("pdf"))
                {

                    if (vm.file != null)
                    {

                        if (System.IO.Directory.Exists(folderPath) == false)
                        {
                            Directory.CreateDirectory(folderPath);
                        }

                        if (Session["Username"] != null)
                        {
                            var fileName = Utilities.TrimSpaces(vm.file.FileName.Replace("'", ""));
                            var fullPath = Path.Combine(folderPath, Path.GetFileName(fileName));


                            //if (Regex.IsMatch(fileName, @"^[a-zA-Z0-9\s.\?\,\'\;\:\#\@\$\%\^\&\*\!\-]+$"))
                            //{
                            //    msg = "Special Character on the filename is  not allowed. Please rename your file!";
                            //}
                            //else
                            //{

                                if (vm.memo.intMstDepartment == 0)
                                {
                                    vm.memo.intMstDepartment = (int)Session["intMstDepartment"];
                                }

                                ViewBag.depts = new SelectList(DeptPrefixDAL.GetDepartmentSP(), "ValueMember", "DisplayMember", (int)Session["intMstDepartment"]);

                                vm.memo.FileName = fileName;
                                msg = MemoDAL.Save(vm.memo);
                                if (msg.Contains("saved"))
                                {
                                    vm.file.SaveAs(fullPath);
                                    vm.memo.isSecondSave = true;
                                    Session["vm"] = vm;
                                    return RedirectToAction("SendTo");
                                }
                                //else
                                //{
                                //    ViewBag.Msg = msg;
                                //    return View();
                                //}

                            //}
                        }  //end user

                    }
                    else{
                        msg = "No file to upload.";
                    }

                }
                else {
                    msg = "PDF file only.";
                }
                
            }
            catch (Exception ex)
            {
                msg = ex.Message.ToString();
            }

            //ViewBag.depts = new SelectList(DeptPrefixDAL.GetDepartmentSP(), "ValueMember", "DisplayMember", (int)Session["intMstDepartment"]);
            ViewBag.Msg = msg;
            return View(vm);        
           // return RedirectToAction("Upload");
