        [HttpPost, CheckSessionOut]
        public ActionResult SendTo(Memo memo)
        {

            string msg = string.Empty;
            bool success = false;

            try
            {
                msg = MemoDAL.SaveUpload(memo);
                if (msg.Contains("saved")) { 
                    success = true;
                    Session["vm"] = null;
                }
            }
            catch (Exception ex)
            {
                success = false;
                msg = ex.Message.ToString();
            }

            return Json(new { success = success.ToString(), msg = msg });
        }

